#!/usr/bin/env node
import { execFileSync } from 'node:child_process'
import { existsSync, readFileSync } from 'node:fs'

const SOURCE_EXTENSIONS = new Set(['java', 'vue', 'ts', 'js', 'mjs', 'css', 'less'])

const EXCLUDED_PATTERNS = [
  /(^|\/)(target|node_modules|dist|coverage|build|vendor)\//,
  /^core\/core-frontend\/public\//,
  /^core\/core-frontend\/src\/locales\//,
  /^core\/core-frontend\/auto-imports\.d\.ts$/,
  /\.d\.ts$/,
  /(^|\/)(assets|svg|png|jpg|jpeg|woff|ttf)\//
]

const DEFAULT_BANNED_COMMENT_PATTERNS = [
  /@author\b/i,
  /created by/i,
  /generated by/i,
  /chatgpt/i,
  /claude/i,
  /codex/i,
  /openai/i
]

const options = {
  json: process.argv.includes('--json'),
  strict: process.argv.includes('--strict')
}

const banlistFile = process.env.COMMENT_AUDIT_BANLIST_FILE
const bannedCommentPatterns = [
  ...DEFAULT_BANNED_COMMENT_PATTERNS
]
const bannedCommentTerms = readBanlistTerms(banlistFile)

// 读取外部注释禁用词配置
function readBanlistTerms(file) {
  if (!file || !existsSync(file)) {
    return []
  }
  return readFileSync(file, 'utf8')
    .split(/\r?\n/)
    .map(line => line.trim())
    .filter(line => line && !line.startsWith('#'))
    .map(line => line.toLowerCase())
}

// 获取仓库中已跟踪的文件列表
function gitFiles() {
  return execFileSync('git', ['ls-files'], { encoding: 'utf8' })
    .split('\n')
    .map(file => file.trim())
    .filter(Boolean)
}

// 判断文件是否需要纳入注释审计
function isAuditedSource(file) {
  const extension = file.split('.').pop()
  return SOURCE_EXTENSIONS.has(extension) && !EXCLUDED_PATTERNS.some(pattern => pattern.test(file))
}

// 将文件路径归类到统计区域
function areaFor(file) {
  if (file.startsWith('core/core-backend/src/main/java/io/crest/')) {
    return `backend/${file.split('/')[7] || 'root'}`
  }
  if (file.startsWith('core/core-backend/src/test/java/io/crest/')) {
    return `backend-test/${file.split('/')[7] || 'root'}`
  }
  if (file.startsWith('core/core-frontend/src/')) {
    return `frontend/${file.split('/')[3] || 'root'}`
  }
  if (file.startsWith('sdk/')) {
    return `sdk/${file.split('/')[1] || 'root'}`
  }
  return file.split('/')[0]
}

// 统计文件中的代码行、注释行和空行
function commentStats(text, extension) {
  const lines = text.split(/\r?\n/)
  let code = 0
  let comment = 0
  let blank = 0
  let blockComment = false
  let htmlComment = false
  const comments = []

  lines.forEach((rawLine, index) => {
    const line = rawLine.trim()
    if (!line) {
      blank += 1
      return
    }

    if (htmlComment) {
      comment += 1
      comments.push({ line: index + 1, text: rawLine })
      if (line.includes('-->')) htmlComment = false
      return
    }

    if (blockComment) {
      comment += 1
      comments.push({ line: index + 1, text: rawLine })
      if (line.includes('*/')) blockComment = false
      return
    }

    if (extension === 'vue' && line.startsWith('<!--')) {
      comment += 1
      comments.push({ line: index + 1, text: rawLine })
      if (!line.includes('-->')) htmlComment = true
      return
    }

    if (line.startsWith('//')) {
      comment += 1
      comments.push({ line: index + 1, text: rawLine })
      return
    }

    if (line.startsWith('/*')) {
      comment += 1
      comments.push({ line: index + 1, text: rawLine })
      if (!line.includes('*/')) blockComment = true
      return
    }

    code += 1
  })

  return { lines: lines.length, code, comment, blank, comments }
}

// 统计声明数量和声明前注释覆盖情况
function declarationStats(text, extension) {
  const lines = text.split(/\r?\n/)
  const declarations = []

  lines.forEach((rawLine, index) => {
    const line = rawLine.trim()
    if (!line || line.startsWith('//') || line.startsWith('*')) {
      return
    }
    if (isDeclaration(line, extension)) {
      declarations.push({
        line: index + 1,
        text: line,
        documented: hasNearbyComment(lines, index, extension),
        mechanical: extension === 'java' && isMechanicalAccessor(line)
      })
    }
  })

  const total = declarations.length
  const documented = declarations.filter(item => item.documented).length
  const mechanical = declarations.filter(item => item.mechanical).length
  const undocumented = declarations.filter(item => !item.documented)

  return { total, documented, mechanical, undocumented }
}

// 判断单行代码是否为需要注释覆盖的声明
function isDeclaration(line, extension) {
  if (extension === 'java') {
    if (/^(if|for|while|switch|catch)\s*\(/.test(line)) {
      return false
    }
    if (!/^(public|protected|private)\s+/.test(line)) {
      return false
    }
    if (/\b(class|interface|enum|record)\s+[A-Za-z0-9_$]+/.test(line)) {
      return true
    }

    const firstEquals = line.indexOf('=')
    const firstParen = line.indexOf('(')
    if (firstEquals !== -1 && firstParen !== -1 && firstEquals < firstParen) {
      return false
    }

    return /^(public|protected|private)\s+(?:(?:static|final|synchronized|abstract|native|default)\s+)*(?:<[^>]+>\s+)?[\w$<>\[\], ?.]+\s+[A-Za-z_$][\w$]*\s*\([^;]*\)\s*(?:throws\s+[\w$<>\[\], ?.]+)?[{;]?$/.test(line)
  }

  if (['ts', 'js', 'mjs', 'vue'].includes(extension)) {
    return /^(export\s+)?(async\s+)?function\s+\w+/.test(line)
      || /^(export\s+)?(const|let|var)\s+\w+\s*=\s*(async\s*)?(\([^)]*\)|[\w$]+)\s*=>/.test(line)
      || /^(export\s+)?const\s+\w+\s*=\s*(computed|watch|reactive|ref|shallowRef|defineProps|defineEmits|defineAsyncComponent)\b/.test(line)
      || /^watch\(/.test(line)
  }

  return false
}

// 判断声明上方是否存在相邻注释
function hasNearbyComment(lines, index, extension) {
  let cursor = index - 1
  while (cursor >= 0 && lines[cursor].trim() === '') {
    cursor -= 1
  }

  while (cursor >= 0 && /^@\w+/.test(lines[cursor].trim())) {
    cursor -= 1
    while (cursor >= 0 && lines[cursor].trim() === '') {
      cursor -= 1
    }
  }

  if (cursor < 0) {
    return false
  }

  const previous = lines[cursor].trim()
  if (previous.startsWith('//')) {
    return true
  }
  if (extension === 'vue' && previous.includes('-->')) {
    return findBlockStart(lines, cursor, '<!--', '-->')
  }
  if (previous.includes('*/')) {
    return findBlockStart(lines, cursor, '/**', '*/') || findBlockStart(lines, cursor, '/*', '*/')
  }
  return false
}

// 在声明附近向上查找块注释起点
function findBlockStart(lines, index, startMarker, endMarker) {
  for (let cursor = index; cursor >= Math.max(0, index - 80); cursor -= 1) {
    if (lines[cursor].includes(startMarker)) {
      return true
    }
    if (cursor !== index && lines[cursor].includes(endMarker)) {
      return false
    }
  }
  return false
}

// 判断 Java 声明是否为机械 getter、setter 或 is 方法
function isMechanicalAccessor(line) {
  return /\b(get|set|is)[A-Z][A-Za-z0-9_$]*\s*\(/.test(line)
}

// 检查注释内容是否命中禁用词
function commentIssues(comments) {
  return comments.flatMap(comment => {
    const lowered = comment.text.toLowerCase()
    const matches = [
      ...bannedCommentPatterns
        .filter(pattern => pattern.test(comment.text))
        .map(pattern => pattern.source),
      ...bannedCommentTerms.filter(term => lowered.includes(term))
    ]
    return matches.map(pattern => ({
      line: comment.line,
      pattern,
      text: comment.text.trim().slice(0, 160)
    }))
  })
}

// 构造空统计聚合对象
function emptyAggregate() {
  return {
    files: 0,
    lines: 0,
    code: 0,
    comment: 0,
    blank: 0,
    declarations: 0,
    documentedDeclarations: 0,
    mechanicalDeclarations: 0
  }
}

// 将单文件统计合并到聚合桶
function addAggregate(map, key, stats) {
  const aggregate = map.get(key) || emptyAggregate()
  aggregate.files += 1
  aggregate.lines += stats.lines
  aggregate.code += stats.code
  aggregate.comment += stats.comment
  aggregate.blank += stats.blank
  aggregate.declarations += stats.declarations
  aggregate.documentedDeclarations += stats.documentedDeclarations
  aggregate.mechanicalDeclarations += stats.mechanicalDeclarations
  map.set(key, aggregate)
}

// 安全计算比例
function ratio(numerator, denominator) {
  if (!denominator) {
    return 0
  }
  return numerator / denominator
}

const byArea = new Map()
const byExtension = new Map()
const files = []
const issueList = []
const totals = emptyAggregate()

for (const file of gitFiles().filter(isAuditedSource)) {
  const extension = file.split('.').pop()
  const text = readFileSync(file, 'utf8')
  const lineStats = commentStats(text, extension)
  const declaration = declarationStats(text, extension)
  const stats = {
    ...lineStats,
    declarations: declaration.total,
    documentedDeclarations: declaration.documented,
    mechanicalDeclarations: declaration.mechanical
  }
  const fileEntry = {
    file,
    extension,
    area: areaFor(file),
    ...stats,
    commentToCode: ratio(stats.comment, stats.code),
    declarationCoverage: ratio(stats.documentedDeclarations, stats.declarations),
    undocumentedExamples: declaration.undocumented.slice(0, 5).map(item => ({
      line: item.line,
      text: item.text.slice(0, 160)
    }))
  }
  files.push(fileEntry)
  addAggregate(byArea, fileEntry.area, stats)
  addAggregate(byExtension, extension, stats)
  addAggregate(new Map([['total', totals]]), 'total', stats)

  for (const issue of commentIssues(lineStats.comments)) {
    issueList.push({ file, ...issue })
  }
}

const largeLowCommentFiles = files
  .filter(file => file.code >= 400)
  .sort((left, right) => left.commentToCode - right.commentToCode || right.code - left.code)
  .slice(0, 40)

const lowDeclarationCoverageFiles = files
  .filter(file => file.declarations >= 5)
  .sort((left, right) => left.declarationCoverage - right.declarationCoverage || right.declarations - left.declarations)
  .slice(0, 40)

const report = {
  auditedExtensions: [...SOURCE_EXTENSIONS].sort(),
  totals: {
    ...totals,
    commentToCode: ratio(totals.comment, totals.code),
    declarationCoverage: ratio(totals.documentedDeclarations, totals.declarations)
  },
  byArea: mapToRows(byArea),
  byExtension: mapToRows(byExtension),
  largeLowCommentFiles,
  lowDeclarationCoverageFiles,
  bannedCommentIssues: issueList
}

if (options.json) {
  console.log(JSON.stringify(report, null, 2))
} else {
  printReport(report)
}

if (options.strict && (issueList.length > 0 || report.totals.declarationCoverage < 1)) {
  process.exitCode = 1
}

// 将聚合 Map 转换为排序后的行数据
function mapToRows(map) {
  return [...map.entries()]
    .map(([name, value]) => ({
      name,
      ...value,
      commentToCode: ratio(value.comment, value.code),
      declarationCoverage: ratio(value.documentedDeclarations, value.declarations)
    }))
    .sort((left, right) => right.code - left.code)
}

// 输出人类可读的审计报告
function printReport(report) {
  console.log('Comment coverage audit')
  console.log('======================')
  console.log(`Files: ${report.totals.files}`)
  console.log(`Code lines: ${report.totals.code}`)
  console.log(`Comment lines: ${report.totals.comment}`)
  console.log(`Comment/code: ${formatPercent(report.totals.commentToCode)}`)
  console.log(`Declarations: ${report.totals.declarations}`)
  console.log(`Documented declarations: ${report.totals.documentedDeclarations}`)
  console.log(`Declaration coverage: ${formatPercent(report.totals.declarationCoverage)}`)
  console.log(`Banned comment issues: ${report.bannedCommentIssues.length}`)

  printTable('\nBy area', report.byArea.slice(0, 30))
  printTable('\nBy extension', report.byExtension)

  console.log('\nLarge files with low comment density')
  report.largeLowCommentFiles.slice(0, 20).forEach(file => {
    console.log(`${formatPercent(file.commentToCode).padStart(7)}  ${String(file.code).padStart(6)} code  ${file.file}`)
  })

  console.log('\nFiles with low declaration coverage')
  report.lowDeclarationCoverageFiles.slice(0, 20).forEach(file => {
    console.log(`${formatPercent(file.declarationCoverage).padStart(7)}  ${String(file.documentedDeclarations).padStart(4)}/${String(file.declarations).padEnd(4)}  ${file.file}`)
    file.undocumentedExamples.slice(0, 2).forEach(example => {
      console.log(`         ${example.line}: ${example.text}`)
    })
  })

  if (report.bannedCommentIssues.length) {
    console.log('\nBanned comment issues')
    report.bannedCommentIssues.slice(0, 40).forEach(issue => {
      console.log(`${issue.file}:${issue.line}  ${issue.text}`)
    })
  }
}

// 输出分组统计表
function printTable(title, rows) {
  console.log(title)
  rows.forEach(row => {
    console.log(
      `${row.name.padEnd(28)} ${String(row.files).padStart(5)} files  `
      + `${String(row.code).padStart(7)} code  `
      + `${formatPercent(row.commentToCode).padStart(7)} comments  `
      + `${formatPercent(row.declarationCoverage).padStart(7)} decls`
    )
  })
}

// 格式化百分比
function formatPercent(value) {
  return `${(value * 100).toFixed(1)}%`
}
